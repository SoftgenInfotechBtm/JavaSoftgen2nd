package com.LipsaTutorialjava.javaBasics;

public abstract class AbstractClassImplement {
	abstract void start();

	abstract void stop();

	public void speedDisplay() {
		System.out.println("Speed of the car is 120kps");
	}

	public void colorDisp() {
		System.out.println("color is red");
	}

}